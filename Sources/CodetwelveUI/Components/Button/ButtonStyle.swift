import SwiftUI

/// The available button styles in the CodeTwelve UI system
public enum ButtonStyle {
    /// Primary button style, typically used for main actions
    case primary
    
    /// Secondary button style, typically used for alternative actions
    case secondary
    
    /// Destructive button style, typically used for dangerous actions
    case destructive
    
    /// Outline button style, typically used for secondary actions
    case outline
    
    /// Ghost button style, typically used for subtle actions
    case ghost
    
    /// Link button style, typically used for navigation or text-like actions
    case link
} 